main() {
  // Operadores Atribuiçao (Binario/Infix)

  double a = 2;
  a = a + 3;
  a += 3;
  a -= 3;
  a *= 3;
  a /= 5;
  a %= 2;

  print(a);

  // Operadores relacionais (Binario/Infix) -> resultado sempre é bool
  num b = 3;
  print(b > 2);
  print(b >= 3);
  print(b < 4);
  print(b <= 4);
  print(b != 3);
  print(b == 3);

}
